class MemberDetailModel {
  final String firstName;
  final String lastName;
  final String email;
  final String phone;
  final String address;
  final String dob;
  final String height;
  final String weight;
  final String batch;
  final bool status;


  MemberDetailModel({
    required this.firstName,
    required this.lastName,
    required this.email,
    required this.phone,
    required this.address,
    required this.dob,
    required this.height,
    required this.weight,
    required this.batch,
    required this.status,
  });
}
